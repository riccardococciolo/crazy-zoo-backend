package com.betacom.cz.response;

public class ResponseBase {
	
	private Boolean rc;
	private String msg;
	
	//Getters&Setters
	public Boolean getRc() {
		return rc;
	}
	public void setRc(Boolean rc) {
		this.rc = rc;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	
}
